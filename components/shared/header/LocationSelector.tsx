export default function LocationSelector() {
  const [selectedCountry, setSelectedCountry] = useState<string>('Spain'); // Valor inicial
  const [isLocationFormOpen, setIsLocationFormOpen] = useState<boolean>(false);
  const [postcode, setPostcode] = useState<string>('');

  // Inicializar el país desde localStorage después del montaje
  useEffect(() => {
    const savedCountry = localStorage.getItem('selectedCountry');
    if (savedCountry) {
      setSelectedCountry(savedCountry);
    }
  }, []);

  // Actualizar localStorage cuando cambie el país seleccionado
  useEffect(() => {
    localStorage.setItem('selectedCountry', selectedCountry);
  }, [selectedCountry]);

  const handleLocationClick = () => {
    setIsLocationFormOpen(true);
  };

  const handleFormSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    console.log('Ubicación seleccionada:', { country: selectedCountry, postcode });
    setIsLocationFormOpen(false);
  };

  return (
    <>
      <button
        onClick={handleLocationClick}
        className="ml-2 flex items-center text-sm text-white hover:underline"
      >
        <MapPin className="w-4 h-4 mr-1" />
        Deliver to {selectedCountry}
      </button>

      {isLocationFormOpen && (
        <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
          <div className="bg-white w-full max-w-md relative">
            <div className="bg-gray-200 p-4 flex justify-between items-center border-b">
              <h2 className="text-lg font-semibold text-black">Choose your location</h2>
              <button
                onClick={() => setIsLocationFormOpen(false)}
                className="bg-white text-blue-700 border border-blue-700 rounded-full w-8 h-8 flex items-center justify-center hover:bg-blue-100"
              >
                <X className="w-5 h-5" />
              </button>
            </div>
            <div className="p-4 space-y-4">
              <p className="text-sm text-gray-500">
                Delivery options and delivery speeds may vary for different locations
              </p>
              <Button
                variant="outline"
                className="w-full bg-yellow-500 text-black font-medium rounded-full py-2 hover:bg-yellow-600"
              >
                Sign in to see your addresses
              </Button>
              <div className="flex items-center gap-2">
                <Input
                  type="text"
                  value={postcode}
                  onChange={(e) => setPostcode(e.target.value)}
                  placeholder="or enter a UK mainland postcode"
                  className="flex-1 border border-gray-300 rounded-md p-2 text-black"
                />
                <Button
                  type="button"
                  onClick={handleFormSubmit}
                  className="bg-gray-200 text-black border border-gray-300 rounded-full px-4 py-2 hover:bg-gray-300"
                >
                  Apply
                </Button>
              </div>
              <div className="flex items-center gap-2">
                <span className="text-sm text-gray-500">or deliver outside the UK</span>
                <Select value={selectedCountry} onValueChange={setSelectedCountry}>
                  <SelectTrigger className="w-full border border-gray-300 rounded-md bg-gray-200 text-black p-2">
                    <SelectValue placeholder="Select a country" />
                  </SelectTrigger>
                  <SelectContent>
                    {countries.map((country) => (
                      <SelectItem key={country.code} value={country.name}>
                        {country.name}
                      </SelectItem>
                    ))}
                  </SelectContent>
                </Select>
              </div>
            </div>
          </div>
        </div>
      )}
    </>
  );
}